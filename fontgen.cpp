void printFontLineToBinPattern(const char line[6])
{
    std::cout << "0b";
    for (int i = 0; i < 5; ++i)
    {
        std::cout << (line[i] == '#' ? '1' : '0');
    }

    std::cout << ", // " << line << std::endl;
}

